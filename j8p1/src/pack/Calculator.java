package pack;

public class Calculator {

	int add(int a,int b)
	{
		int sum=a+b;
		return sum;
	}
	int sub(int a,int b)
	{
		int diff=a-b;
		return diff;
	}
	int mul(int a,int b)
	{
		int pro=a*b;
		return pro;
	}
	float div(int a,int b)
	{
		float quo=a/b;
		return quo;
	}
}
